class EvenOdd
{
	public static void main(String args[])
	{
		int arr[]={1,2,3,4,5,6,7,8,9};
		int i;
		for(i=0;i<8;i++)
		{
			if(arr[i]%2==0)
			{
				System.out.println("Even");
				System.out.println(arr[i]);
			}
			else
			{										System.out.println("Odd");

			}
		}
	}

}
